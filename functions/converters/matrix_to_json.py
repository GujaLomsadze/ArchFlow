import json
import random
import string


def generate_random_names(num_nodes, name_length=5):
    """
    Generate a list of unique random names for graph nodes.
    :param num_nodes: Number of unique node names to generate.
    :param name_length: Length of each node name.
    :return: A list of unique node names.
    """
    names = set()
    while len(names) < num_nodes:
        name = ''.join(random.choices(string.ascii_uppercase + string.digits, k=name_length))
        names.add(name)

    return list(names)


def matrix_to_json_with_random_names(matrix, output_file_path, name_length=5):
    """
    Generate a JSON file from an adjacency matrix with randomly generated node names.
    :param matrix: 2D list representing the adjacency matrix.
    :param output_file_path: Path to the output JSON file.
    :param name_length: Length of each node name.
    """

    num_nodes = len(matrix)
    nodes = generate_random_names(num_nodes, name_length)
    data = {"nodes": [], "links": []}

    for node in nodes:
        data["nodes"].append({"id": node})

    for i, row in enumerate(matrix):
        for j, val in enumerate(row):
            if val != 0:  # Assuming 0 means no edge
                data["links"].append({
                    "from": nodes[i],
                    "to": nodes[j],
                    "weight": str(val)  # Include this only if your graph is weighted
                })

    # Write the data to a JSON file
    with open(output_file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=4)


